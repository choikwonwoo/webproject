package vo;

public class AdminImg {
	private String am_code, am_name, am_img, am_num;

	public String getAm_code() {
		return am_code;
	}

	public void setAm_code(String am_code) {
		this.am_code = am_code;
	}

	public String getAm_name() {
		return am_name;
	}

	public void setAm_name(String am_name) {
		this.am_name = am_name;
	}

	public String getAm_img() {
		return am_img;
	}

	public void setAm_img(String am_img) {
		this.am_img = am_img;
	}

	public String getAm_num() {
		return am_num;
	}

	public void setAm_num(String am_num) {
		this.am_num = am_num;
	}
}
